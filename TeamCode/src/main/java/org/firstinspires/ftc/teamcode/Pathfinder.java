public class Pathfinder {

    private static final int MAX_PATH_LENGTH = 300;

    Pathfinder(int startX, int startY, int goalX, int goalY) {
    }

    public List<RobotPosition> generateRobotPath(int startX, int startY, int goalX, int goalY) {

        int START_X = startX + 38;
        int START_Y = 38 - startY;
        int GOAL_X = goalX + 38;
        int GOAL_Y = 38 - goalY;

        SimpleMap map = new SimpleMap();
        AStarPathFinder pathFinder = new AStarPathFinder(map, MAX_PATH_LENGTH, true);
        List<RobotPosition> pathFound = new ArrayList<>();

        if (map.isBlocked(START_X, START_Y) || map.isBlocked(GOAL_X, GOAL_Y)) {
            throw new IllegalArgumentException("Starting or ending point blocked");
        } else {
            Path path = pathFinder.findPath(null, START_X, START_Y, GOAL_X, GOAL_Y);
            int length = path.getLength();

            for (int i = 0; i < length; i++) {
                pathFound.add(new RobotPosition(path.getX(i) - 38, -path.getY(i) + 38));
            }
        }

        List<RobotPosition> monitor = pathFound;

        return pathFound;
    }

}

